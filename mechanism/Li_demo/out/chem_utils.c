void eval_conc (const double T, const double pres, const double * __restrict__ y, double * __restrict__ y_N, double * __restrict__ mw_avg, double * __restrict__ rho, double * __restrict__ conc) {

  // mass fraction of final species
  *y_N = 1.0 - (y[0] + y[1] + y[2] + y[3] + y[4]);
  *mw_avg = (y[0] * 4.9603174603174605e-01) + (y[1] * 6.2503906494155881e-02)
      + (y[2] * 5.5509297807382736e-02) + (y[3] * 9.9206349206349209e-01)
      + (y[4] * 5.8799317927912029e-02) + ((*y_N) * 3.5696437495537948e-02);
  *mw_avg = 1.0 / *mw_avg;
  // mass-averaged density
  *rho = pres * (*mw_avg) / (8.31446210e+03 * T);
  conc[0] = (*rho) * y[0] * 4.9603174603174605e-01;
  conc[1] = (*rho) * y[1] * 6.2503906494155881e-02;
  conc[2] = (*rho) * y[2] * 5.5509297807382736e-02;
  conc[3] = (*rho) * y[3] * 9.9206349206349209e-01;
  conc[4] = (*rho) * y[4] * 5.8799317927912029e-02;
  conc[5] = (*rho) * (*y_N) * 3.5696437495537948e-02;

} // end eval_conc

void eval_conc_rho (const double T, const double rho, const double * __restrict__ y, double * __restrict__ y_N, double * __restrict__ mw_avg, double * __restrict__ pres, double * __restrict__ conc) {

  // mass fraction of final species
  *y_N = 1.0 - (y[0] + y[1] + y[2] + y[3] + y[4]);
  *mw_avg = (y[0] * 4.9603174603174605e-01) + (y[1] * 6.2503906494155881e-02)
      + (y[2] * 5.5509297807382736e-02) + (y[3] * 9.9206349206349209e-01)
      + (y[4] * 5.8799317927912029e-02) + ((*y_N) * 3.5696437495537948e-02);
  *mw_avg = 1.0 / *mw_avg;
  // pressure
  *pres = rho * 8.31446210e+03 * T / (*mw_avg);
  conc[0] = rho * y[0] * 4.9603174603174605e-01;
  conc[1] = rho * y[1] * 6.2503906494155881e-02;
  conc[2] = rho * y[2] * 5.5509297807382736e-02;
  conc[3] = rho * y[3] * 9.9206349206349209e-01;
  conc[4] = rho * y[4] * 5.8799317927912029e-02;
  conc[5] = rho * (*y_N) * 3.5696437495537948e-02;

} // end eval_conc

